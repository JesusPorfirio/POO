    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        String cpfPessoa = txtCpf.getText();
        int x;
        if(Pessoa.validarCPF(cpfPessoa)){
           JOptionPane.showMessageDialog(null, "Número de CPF inválido", "aviso", JOptionPane.ERROR_MESSAGE);
           txtCpf.requestFocus();
        }else{        
            for(x=0; x<cadastroP.size(); x++){
                if(cpfPessoa.equals(cadastroP.get(x).getCpf())){
                    if(cadastroP.get(x) instanceof Vendedor){
                        break;
                    }else if(cadastroP.get(x) instanceof Cliente){

                    }
                }
            }
            if(x < cadastroP.size()){
                posVend = x;
            }else{
                posVend = -1;
            }    

            if(posVend >= 0){
                txtNome.setText(cadastroP.get(posVend).getNome());
                
            }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

